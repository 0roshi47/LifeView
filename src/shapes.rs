use bevy::{
    ecs::system::{Commands, ResMut},
    math::IVec2,
};

use crate::rule::{KernelDef, Rule};

#[derive(Clone, Debug)]
pub struct Shape {
    pub name: String,
    pub optimal_rule: Rule,
    pub cells_state: Vec<f32>,
    pub cells_pos: Vec<IVec2>,
    pub cells_channel: Vec<usize>,
}

impl Shape {
    pub fn new(
        name: String,
        optimal_rule: Rule,
        cells_state: Vec<f32>,
        cells_pos: Vec<IVec2>,
        cells_channel: Vec<usize>,
    ) -> Self {
        Self {
            name,
            optimal_rule,
            cells_state,
            cells_pos,
            cells_channel,
        }
    }

    /// Build from a 2D row-major grid of f32 values, top-left origin.
    /// Zero cells are skipped (not spawned).
    pub fn from_grid(name: impl Into<String>, optimal_rule: Rule, grid: &[&[f32]]) -> Self {
        let rows = grid.len() as i32;
        let cols = grid[0].len() as i32;
        let origin = IVec2::new(cols / 2, rows / 2);
        let mut cells_state = Vec::new();
        let mut cells_pos = Vec::new();
        let mut cells_channel = Vec::new();
        for (y, row) in grid.iter().enumerate() {
            for (x, &val) in row.iter().enumerate() {
                if val > 0.0 {
                    cells_state.push(val);
                    cells_pos.push(IVec2::new(x as i32, rows - 1 - y as i32) - origin);
                    cells_channel.push(0);
                }
            }
        }
        Self::new(name.into(), optimal_rule, cells_state, cells_pos, cells_channel)
    }

    pub fn ring(
        name: impl Into<String>,
        optimal_rule: Rule,
        r_inner: i32,
        r_outer: i32,
        state_fn: impl Fn(f32) -> f32,
    ) -> Self {
        let mut cells_state = Vec::new();
        let mut cells_pos = Vec::new();
        let mut cells_channel = Vec::new();
        for x in -r_outer..=r_outer {
            for y in -r_outer..=r_outer {
                let dist = ((x * x + y * y) as f32).sqrt();
                if dist >= r_inner as f32 && dist <= r_outer as f32 {
                    let t = (dist - r_inner as f32) / (r_outer - r_inner).max(1) as f32;
                    cells_state.push(state_fn(t).clamp(0.0, 1.0));
                    cells_pos.push(IVec2::new(x, y));
                    cells_channel.push(0);
                }
            }
        }
        Self::new(name.into(), optimal_rule, cells_state, cells_pos, cells_channel)
    }

    /// Build from a 2D grid of f32 values for a specific channel.
    /// Zero cells are skipped.
    pub fn from_channel_grid(
        name: impl Into<String>,
        optimal_rule: Rule,
        channel: usize,
        grid: &[&[f32]],
    ) -> Self {
        let rows = grid.len() as i32;
        let cols = grid[0].len() as i32;
        let origin = IVec2::new(cols / 2, rows / 2);
        let mut cells_state = Vec::new();
        let mut cells_pos = Vec::new();
        let mut cells_channel = Vec::new();
        for (y, row) in grid.iter().enumerate() {
            for (x, &val) in row.iter().enumerate() {
                if val > 0.0 {
                    cells_state.push(val);
                    cells_pos.push(IVec2::new(x as i32, rows - 1 - y as i32) - origin);
                    cells_channel.push(channel);
                }
            }
        }
        Self::new(name.into(), optimal_rule, cells_state, cells_pos, cells_channel)
    }

    /// Merge cells from another shape into this one (for multi-channel shapes).
    pub fn merge_channel(mut self, other: Self) -> Self {
        self.cells_state.extend(other.cells_state);
        self.cells_pos.extend(other.cells_pos);
        self.cells_channel.extend(other.cells_channel);
        self
    }

    /// Orbium bicaudatus seed from the official Lenia Colab tutorial
    /// (OpenLenia/Lenia-Tutorial: Tutorial_From_Conway_to_Lenia).
    /// Parameters: R=13, mu=0.15, sigma=0.015, T=10
    pub fn orbium_bicaudatus(name: impl Into<String>) -> Self {
        let rule = Rule::single_channel(0.15, 0.015, 13);
        Self::from_grid(name, rule, ORBIUM_GRID)
    }

    /// Hydrogeminium natans — 51×55 grid from Wartets/Lenia (AnimalData.inc s_animal_200).
    /// Parameters: R=18, mu=0.26, sigma=0.036, peaks=[0.5, 1.0, 0.667], T=10
    pub fn hydrogeminium_natans(name: impl Into<String>) -> Self {
        let mut kernel = KernelDef::default_single(0.26, 0.036, 18);
        kernel.peaks = vec![0.5, 1.0, 0.667];
        let rule = Rule::multi_channel(vec![kernel], 1);
        Self::from_grid(name, rule, HYDROGEMINIUM_GRID)
    }

    /// Fish (multi-kernel) — 21×22 grid from official Lenia Colab tutorial.
    /// Parameters: R=10, T=5, 3 kernels averaging growth
    pub fn fish(name: impl Into<String>) -> Self {
        let kernels = vec![
            // Kernel 0: b=[1, 5/12, 2/3], mu=0.156, sigma=0.0118
            {
                let mut k = KernelDef::default_single(0.156, 0.0118, 10);
                k.peaks = vec![1.0, 5.0 / 12.0, 2.0 / 3.0];
                k
            },
            // Kernel 1: b=[1/12, 1], mu=0.193, sigma=0.049
            {
                let mut k = KernelDef::default_single(0.193, 0.049, 10);
                k.peaks = vec![1.0 / 12.0, 1.0];
                k
            },
            // Kernel 2: b=[1], mu=0.342, sigma=0.0891
            {
                let mut k = KernelDef::default_single(0.342, 0.0891, 10);
                k.peaks = vec![1.0];
                k
            },
        ];
        let mut rule = Rule::multi_channel(kernels, 1);
        rule.delta = 0.2; // T=5 => dt = 1/T = 0.2
        Self::from_grid(name, rule, FISH_GRID)
    }

    /// Tessellatium gyrans — 3-channel, 15 kernels, 18×24 grids per channel.
    /// Parameters: R=12, T=2 (dt=0.5)
    pub fn tessellatium_gyrans(name: impl Into<String>) -> Self {
        let kernels = vec![
            // Channel 0 -> Channel 0 (kernels 0-2)
            KernelDef::new(0.272, 0.0595, 12, 0.91, 0.138, vec![1.0], 0, 0),
            KernelDef::new(0.349, 0.1585, 12, 0.62, 0.48, vec![1.0], 0, 0),
            KernelDef::new(0.2, 0.0332, 12, 0.5, 0.284, vec![1.0, 0.25], 0, 0),
            // Channel 1 -> Channel 1 (kernels 3-5)
            KernelDef::new(0.114, 0.0528, 12, 0.97, 0.256, vec![0.0, 1.0], 1, 1),
            KernelDef::new(0.447, 0.0777, 12, 0.72, 0.5, vec![1.0], 1, 1),
            KernelDef::new(0.247, 0.0342, 12, 0.8, 0.622, vec![5.0 / 6.0, 1.0], 1, 1),
            // Channel 2 -> Channel 2 (kernels 6-8)
            KernelDef::new(0.21, 0.0617, 12, 0.96, 0.35, vec![1.0], 2, 2),
            KernelDef::new(0.462, 0.1192, 12, 0.56, 0.218, vec![1.0], 2, 2),
            KernelDef::new(0.446, 0.1793, 12, 0.78, 0.556, vec![1.0], 2, 2),
            // Cross-channel: 0->1 (kernel 9)
            KernelDef::new(0.327, 0.1408, 12, 0.79, 0.344, vec![11.0 / 12.0, 1.0], 0, 1),
            // Cross-channel: 0->2 (kernel 10)
            KernelDef::new(0.476, 0.0995, 12, 0.5, 0.456, vec![0.75, 1.0], 0, 2),
            // Cross-channel: 1->0 (kernel 11)
            KernelDef::new(0.379, 0.0697, 12, 0.72, 0.67, vec![11.0 / 12.0, 1.0], 1, 0),
            // Cross-channel: 1->2 (kernel 12)
            KernelDef::new(0.262, 0.0877, 12, 0.68, 0.42, vec![1.0], 1, 2),
            // Cross-channel: 2->0 (kernel 13)
            KernelDef::new(0.412, 0.1101, 12, 0.82, 0.43, vec![1.0 / 6.0, 1.0, 0.0], 2, 0),
            // Cross-channel: 2->1 (kernel 14)
            KernelDef::new(0.201, 0.0786, 12, 0.82, 0.278, vec![1.0], 2, 1),
        ];
        let mut rule = Rule::multi_channel(kernels, 3);
        rule.delta = 0.5; // T=2 => dt = 1/T = 0.5

        // Build shape from 3 separate channel grids
        let ch0 = Self::from_channel_grid("", rule.clone(), 0, TESSELLATIUM_CH0);
        let ch1 = Self::from_channel_grid("", rule.clone(), 1, TESSELLATIUM_CH1);
        let ch2 = Self::from_channel_grid("", rule.clone(), 2, TESSELLATIUM_CH2);

        Shape::new(name.into(), rule, Vec::new(), Vec::new(), Vec::new())
            .merge_channel(ch0)
            .merge_channel(ch1)
            .merge_channel(ch2)
    }
}

